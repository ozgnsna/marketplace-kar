export function getTrendyolKargoExclVat(
  desi: number,
  carrier: TrendyolCarrierId
): number | null {
  const d = Math.round(desi);
  const rowData = TRENDYOL_KARGO_EXCL_VAT[d];
  if (!rowData) return null;
  const v = rowData[carrier];
  return typeof v === "number" ? v : null;
}

/** PDF KDV hariç → formda kullanılan KDV dahil (+%20) */
export function trendyolKargoToInclVat(excl: number): number {
  return Math.round(excl * 1.2 * 100) / 100;
}
